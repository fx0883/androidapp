package com.ChildHealthDiet.app2.model.bean;

public class MineItem {
    private String key;
    private String name;
    private String picture;
    private String jumpClassName;
    private String jumpingParam;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getJumpClassName() {
        return jumpClassName;
    }

    public void setJumpClassName(String jumpClassName) {
        this.jumpClassName = jumpClassName;
    }

    public String getJumpingParam() {
        return jumpingParam;
    }

    public void setJumpingParam(String jumpingParam) {
        this.jumpingParam = jumpingParam;
    }
}
